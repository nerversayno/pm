public class LambadaEntity {
   private int i;
    private String name;

    public LambadaEntity(int i, String s) {
        this.i = i;
        this.name = s;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
